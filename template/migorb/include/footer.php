$(document).ready(function() {

	$('#owl-one').owlCarousel({
		loop: true,
		margin: 10,
		nav: true,
		autoplay: true,
		smartSpeed: 1500,
		responsive: {
			0: {
				items: 1
			},
			600: {
				items: 1
			},
			1000: {
				items: 2
			}
		}
	});

	$('#owl-two').owlCarousel({
		loop: true,
		margin: 10,
		nav: true,
		autoplay: true,
		smartSpeed: 1500,
		responsive: {
			0: {
				items: 2
			},
			600: {
				items: 3
			},
			1000: {
				items: 8
			}
		}
	});


	$('#submit').click(function(e) {
		e.preventDefault();
		var name = $("#name").val();
		var email = $("#email").val();
		var subject1 = $("#subject1").val();
		var message = $("#message").val();
		$.ajax({
			type: "POST",
			url: "mail.php",
			dataType: "json",
			data: {
				name: name,
				email: email,
				subject1: subject1,
				message: message
			},
			success: function(data) {
				//console.log(data);
				if(data.code == "200") {
					$('#successMSG').html(data.successEmail);
					$('#name').val("");
					$('#email').val("");
					$('#subject1').val("");
					$('#message').val("");
					$(".display-error1").css("display", "none");
					$(".display-error2").css("display", "none");
					$(".display-error3").css("display", "none");
					$(".display-error4").css("display", "none");
					$('#successMSG').fadeIn();
					setTimeout(function() {
						$('#successMSG').hide();
					}, 6000);
				} else {
					$(".display-error1").html(data.msg1);
					$(".display-error1").css("display", "block");
					$(".display-error2").html(data.msg2);
					$(".display-error2").css("display", "block");
					$(".display-error3").html(data.msg3);
					$(".display-error3").css("display", "block");
					$(".display-error4").html(data.msg4);
					$(".display-error4").css("display", "block");
				}
			}
		});
	});

	//on click active tab and deactivate other tabs
	$('.btn-link').click(function(){
		$(this).addClass("active-btn");
		$(this).parents(".card").siblings().find("button").removeClass("active-btn");
		$(this).attr("aria-expanded", "true");
		$(this).parents(".card").find(".collapse").slideDown();
		$(this).parents(".card").siblings("button").attr("aria-expanded", "false");
		$(this).parents(".card").siblings().find(".collapse").slideUp();
	});

	//active based hash
	let hash = window.location.hash;
	$(hash).addClass("show");
	let btntab = $(hash).parent("card-header").find("button");
	$(btntab).attr("aria-expanded", "true");
	let tabSiblings = $(hash).parent(".card").siblings();
	$(tabSiblings).find("button").attr("aria-expanded", "false");
	$(tabSiblings).find(".collapse").slideUp();

	//Activate tab right icon to down based on hash
	let currenttab = $(hash).parents(".card");
	$(currenttab).find(".btn-link").addClass("active-btn");
	
	//adding first item arrow down on direct services page visit
	$("#accordionExample .card:first").find('button').addClass("active-btn");



	$("#client-register").click(function(){
		$("#login-form")[0].reset();
        $("#login-form-wrap").hide();
        $("#register-form-wrap").show();
    });

    $("#client-login").click(function(){
    	$("#register-form")[0].reset()
        $("#login-form-wrap").show();
        $("#register-form-wrap").hide();
    });



    //clientmenu


});






